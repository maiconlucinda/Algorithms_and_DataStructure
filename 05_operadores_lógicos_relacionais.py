# OPERADORES RELACIONAIS
maior = 5 > 3
print(maior)

menor = 5 < 3
print(menor)

maiorIgual = 5 >= 3
print(maiorIgual)

menorIgual = 5 <= 3
print(menorIgual)

operadorIgualdade = 5 == 3 #validando se um número é igual a outro número
print(operadorIgualdade)

operadorDiferente = 5 != 3 #validando se um número é diferente do outro número
print(operadorDiferente)


